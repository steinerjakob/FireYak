/**
 * Generates src/theme/md3/specs/theme.css from the brand seed color.
 *
 * Scheme: Material Design 3 "Fidelity" — keeps the primary close to the
 * brand chroma (vivid orange from the app icon) instead of the muted
 * TonalSpot mapping. Tertiary resolves to the icon's sky blue.
 *
 * @material/material-color-utilities ships ESM with extensionless imports,
 * so run it bundled:
 *
 *   ./node_modules/.bin/esbuild --bundle --format=cjs --platform=node \
 *     scripts/generate-md3-theme.mjs | node
 */
import { writeFileSync } from 'node:fs';
import {
	argbFromHex,
	hexFromArgb,
	Hct,
	SchemeFidelity,
	MaterialDynamicColors,
	TonalPalette
} from '@material/material-color-utilities';

const SOURCE = '#e66611';
const OUT_FILE = 'src/theme/md3/specs/theme.css';

const seedHct = Hct.fromInt(argbFromHex(SOURCE));
const light = new SchemeFidelity(seedHct, false, 0);
const dark = new SchemeFidelity(seedHct, true, 0);

/* Custom colors keep their established palettes; they were harmonized with
   the (unchanged) source color when first generated. */
const successPalette = TonalPalette.fromInt(argbFromHex('#106d34'));
const warningPalette = TonalPalette.fromInt(argbFromHex('#775a00'));

/* token name (without --md-sys- prefix) -> MaterialDynamicColors property */
const SYS_TOKENS = [
	['primary', 'primary'],
	['on-primary', 'onPrimary'],
	['primary-container', 'primaryContainer'],
	['on-primary-container', 'onPrimaryContainer'],
	['secondary', 'secondary'],
	['on-secondary', 'onSecondary'],
	['secondary-container', 'secondaryContainer'],
	['on-secondary-container', 'onSecondaryContainer'],
	['tertiary', 'tertiary'],
	['on-tertiary', 'onTertiary'],
	['tertiary-container', 'tertiaryContainer'],
	['on-tertiary-container', 'onTertiaryContainer'],
	['error', 'error'],
	['on-error', 'onError'],
	['error-container', 'errorContainer'],
	['on-error-container', 'onErrorContainer'],
	['background', 'background'],
	['on-background', 'onBackground'],
	['surface', 'surface'],
	['on-surface', 'onSurface'],
	['surface-variant', 'surfaceVariant'],
	['on-surface-variant', 'onSurfaceVariant'],
	['outline', 'outline'],
	['outline-variant', 'outlineVariant'],
	['shadow', 'shadow'],
	['scrim', 'scrim'],
	['inverse-surface', 'inverseSurface'],
	['inverse-on-surface', 'inverseOnSurface'],
	['inverse-primary', 'inversePrimary'],
	['surface-container-lowest', 'surfaceContainerLowest'],
	['surface-container-low', 'surfaceContainerLow'],
	['surface-container', 'surfaceContainer'],
	['surface-container-high', 'surfaceContainerHigh'],
	['surface-container-highest', 'surfaceContainerHighest']
];

/* custom token suffix -> [light tone, dark tone] */
const CUSTOM_TONES = [
	['color', 40, 80],
	['on-color', 100, 20],
	['color-container', 90, 30],
	['on-color-container', 10, 90]
];

const PALETTE_TONES = [100, 99, 95, 90, 85, 80, 75, 70, 65, 60, 55, 50, 45, 40, 35, 30, 25, 20, 15, 10, 5, 0];

const hex = (argb) => hexFromArgb(argb);
const rgb = (argb) => `${(argb >> 16) & 0xff}, ${(argb >> 8) & 0xff}, ${argb & 0xff}`;
const sysArgb = (scheme, prop) => MaterialDynamicColors[prop].getArgb(scheme);

const sysPairs = SYS_TOKENS.map(([token, prop]) => ({
	token: `--md-sys-${token}`,
	light: sysArgb(light, prop),
	dark: sysArgb(dark, prop)
}));

const customPairs = [];
for (const [name, palette] of [['success', successPalette], ['warning', warningPalette]]) {
	for (const [suffix, lightTone, darkTone] of CUSTOM_TONES) {
		customPairs.push({
			token: `--md-custom-${name}-${suffix}`,
			light: palette.tone(lightTone),
			dark: palette.tone(darkTone)
		});
	}
}

const lightDarkLines = (pairs) =>
	pairs
		.map(
			(p) =>
				`\t${p.token}: light-dark(${hex(p.light)}, ${hex(p.dark)});\n` +
				`\t${p.token}-rgb: ${rgb(p.light)};`
		)
		.join('\n');

const darkRgbLines = (pairs, indent) =>
	pairs.map((p) => `${indent}${p.token}-rgb: ${rgb(p.dark)};`).join('\n');

const paletteBlocks = [
	['primary', light.primaryPalette],
	['secondary', light.secondaryPalette],
	['tertiary', light.tertiaryPalette],
	['neutral', light.neutralPalette],
	['neutral-variant', light.neutralVariantPalette],
	['error', light.errorPalette]
]
	.map(
		([name, palette]) =>
			PALETTE_TONES.map((t) => `\t--md-ref-palette-${name}${t}: ${hex(palette.tone(t))};`).join('\n')
	)
	.join('\n\n');

const css = `:root {
	/* Material Design 3 color schemes (scheme: Fidelity)
	   Generated by scripts/generate-md3-theme.mjs — do not edit by hand. */
	--md-sys-source: ${SOURCE};

${lightDarkLines(sysPairs)}

	/* Custom colors "success" / "warning" (harmonized with source) */
${lightDarkLines(customPairs)}

	/* Palette tone references */

${paletteBlocks}
}

/* light-dark() only accepts <color> values, so the bare r, g, b triplets
   above (consumed by Ionic as rgba(var(--…-rgb), alpha)) need explicit
   dark-scheme overrides mirroring the toggles in variables.scss. */
@media (prefers-color-scheme: dark) {
	:root:not(.prefers-light):not(.ion-palette-light) {
${darkRgbLines([...sysPairs, ...customPairs], '\t\t')}
	}
}

:root.prefers-dark,
:root.ion-palette-dark {
${darkRgbLines([...sysPairs, ...customPairs], '\t')}
}
`;

writeFileSync(OUT_FILE, css);
console.log(`Wrote ${OUT_FILE} (seed ${SOURCE}, Fidelity)`);
console.log('primary light/dark:', hex(sysPairs[0].light), '/', hex(sysPairs[0].dark));
console.log(
	'primary-container light/dark:',
	hex(sysPairs[2].light),
	'/',
	hex(sysPairs[2].dark)
);
